public class CicloFor {
    public static void main(String[] args) {
        // for
        // conteo del 0 al 20
        // For contiene (inicio; condicion e iteraci√≥n)

        for(int i =0; i<=20; i++){
            System.out.println(i);
        }
    System.out.println("---------------------------");
        // conteo del 0 al 20 de 5 en 5
        for(int a =0; a<=20; a+=5){
            System.out.println(a);
    } 
    System.out.println("---------------------------");
    // conteo decreciente
        for(int b =20; b>=0; b-=5){ // uno en uno (--)
            System.out.println(b);
    }
}
}
